for i in range(5):
    line = "+" * i + "#" + "+" * (4-i)
    # +의 개수(앞) + #(1글자) + +의 개수(뒤) = 5
    print(line)


#2중for문 밖->행/안->열 #####################################################
for i in range(5):
    for j in range(5):
        if i == j:
            print("#", end=" ")
        else:
            print("+", end=" ")
    print()

